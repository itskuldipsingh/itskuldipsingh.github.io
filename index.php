Welcome to Kuldip Singh's Professional Portfolio<br>
Greetings! I am Kuldip Singh, a dedicated professional with a passion for electrical engineering and education. I embarked on my educational journey at Jitendra Smarak Inter College Narayanpur Kothi, Kushinagar, where I completed my High School (10th) in 2016.<br>
Building upon this foundation, I pursued a Diploma in Electrical Engineering (Industrial Control) from Government Polytechnic Deoria. This academic endeavor equipped me with essential skills and knowledge in the field of industrial control systems.<br>
To further enhance my expertise, I obtained the esteemed National Craft Instructor Certificate (NCIC) from NSTI Dehradun, a testament to my commitment to excellence in education and training.<br>
Drawing upon my comprehensive education, I delved into the realm of teaching, accumulating valuable experience over two years as an electrician instructor at Deoria Pvt ITI. This role not only allowed me to share my knowledge but also refine my understanding of effective pedagogical methods.<br>
Currently, I am privileged to serve as a Junior Technician in the Department of Cognitive Science at the esteemed Indian Institute of Technology Kanpur. Here, I contribute to cutting-edge research and innovation, leveraging my technical proficiency in support of groundbreaking projects.<br>
This platform is designed to provide you with insights into my educational background, professional experiences, and areas of expertise. Feel free to explore my portfolio and do not hesitate to reach out for further information or collaboration opportunities.<br>
Thank you for visiting, and I look forward to connecting with you!<br>

